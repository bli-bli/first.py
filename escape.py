# \n : 줄바꿈
print("백문이 불여일견 \n백문이 불여일타")

# 따옴표
print("저는 '조은비' 입니다")
print('저는 "조은비" 입니다')

# \" \' : 문장 내에서 그대로 따옴표를 사용
print("저는 \"조은비\" 입니다")

# \\ : 문장 내에서 \
print("C:\\Users\\Eunbi\\Desktop")

# \r : 커서를 맨 앞으로 이동
print("Red Apple\rPine") # Pine Apple

# \b : 백스페이스 , 바로 앞에 한글자 삭제
print("Pinee\bApple") # Pine Apple

# \t : 탭
print("Red\tApple") # Red Apple